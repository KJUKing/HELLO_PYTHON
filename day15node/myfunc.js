function add(a, b) {
    return a + b;
}

function min(a, b) {
    return a - b;
}

function mul(a, b) {
    return a * b;
}
function div(a, b) {
    return a / b;
}
sum = add(4, 2);
min = min(4, 2);
mul = mul(4, 2);
div = div(4, 3);
console.log("sum : ", sum);
console.log("min : ", min);
console.log("mul : ", mul);
